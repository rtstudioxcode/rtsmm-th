export function computeLevel(totalSpent = 0) {
  let lvl = 1;
  for (let i = 0; i < LEVELS.length; i++) {
    if (totalSpent >= LEVELS[i].need) lvl = i + 1;
    else break;
  }
  return lvl;
}

export function nextNeedFor(level) {
  const idx = Math.min(level, LEVELS.length - 1);
  return LEVELS[idx]?.need ?? LEVELS.at(-1).need;
}
